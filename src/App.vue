<template>
  <div id="app">
    <div v-if="!isAuth">
      <div class="loader">Loading...</div>
      <p class="loader-text">
        Белтелерадиокомпания<br />
        Данная страница доступна определенному кругу лиц. <br />Для доступа к
        данной странице Вам необходимо авторизоваться.<br />
        Вы будете перенаправлены на главную страницу.
      </p>
    </div>
    <div v-else>
      <router-view></router-view>
    </div>
  </div>
</template>

<script>
import { auth } from '@/api/company.js'
export default {
  name: 'App',
  data() {
    return { isAuth: false }
  },
  async created() {
    await this.isAuthorization()
  },

  methods: {
    async isAuthorization() {
      let { status } = await auth()
      this.isAuth = status
      setTimeout(function () {
        if (!status) window.location.href = 'https://www.tvr.by/'
      }, 2500)
    },
  },
}
</script>

